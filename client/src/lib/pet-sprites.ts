/** スプライトの状態を気分から決定 */
export function getSpriteState(
  mood: PetMood,
  isActive: boolean
): "idle" | "walk" | "sleep" {
  if (mood === "sleepy" || !isActive) return "sleep";
  if (mood === "happy") return "walk";
  return "idle";
}

/** Canvas上にピクセルスプライトを描画 */
export function drawSprite(
  ctx: CanvasRenderingContext2D,
  species: PetSpecies,
  state: "idle" | "walk" | "sleep",
  x: number,
  y: number,
  pixelSize: number
): void {
  const grid = SPRITES[species]?.[state] ?? SPRITES[species]?.idle;
  if (!grid) return;

  for (let row = 0; row < grid.length; row++) {
    for (let col = 0; col < grid[row].length; col++) {
      const color = grid[row][col];
      if (color) {
        ctx.fillStyle = color;
        ctx.fillRect(
          x + col * pixelSize,
          y + row * pixelSize,
          pixelSize,
          pixelSize
        );
      }
    }
  }
}

