// =====================================================================
// PLAYER_HIT (16x24) — 被弾（IDLEに赤オーバーレイ）
// =====================================================================
function applyRedTint(grid: PixelGrid): PixelGrid {
  return grid.map(row =>
    row.map(c => {
      if (c === null) return null;
      // 16進カラーをパースして赤みを加える
      if (c.startsWith("#") && c.length === 7) {
        const r = Math.min(255, parseInt(c.slice(1, 3), 16) + 80);
        const g = Math.max(0, parseInt(c.slice(3, 5), 16) - 30);
        const b = Math.max(0, parseInt(c.slice(5, 7), 16) - 30);
        return `#${r.toString(16).padStart(2, "0")}${g.toString(16).padStart(2, "0")}${b.toString(16).padStart(2, "0")}`;
      }
      return c;
    })
  );
}

export const PLAYER_HIT: PixelGrid = applyRedTint(PLAYER_IDLE);

// =====================================================================
// PLAYER_DEAD (16x24) — 戦死（IDLEベースを暗くする）
// =====================================================================
function applyDarken(grid: PixelGrid): PixelGrid {
  return grid.map(row =>
    row.map(c => {
      if (c === null) return null;
      if (c.startsWith("#") && c.length === 7) {
        const r = Math.max(0, parseInt(c.slice(1, 3), 16) - 60);
        const g = Math.max(0, parseInt(c.slice(3, 5), 16) - 60);
        const b = Math.max(0, parseInt(c.slice(5, 7), 16) - 60);
        return `#${r.toString(16).padStart(2, "0")}${g.toString(16).padStart(2, "0")}${b.toString(16).padStart(2, "0")}`;
      }
      return c;
    })
  );
}

