// CROSSHAIR (16x16) — 赤い照準器（プログラム生成）
// =====================================================================
export const CROSSHAIR: PixelGrid = (() => {
  const size = 16;
  const grid: PixelGrid = Array.from({ length: size }, () =>
    Array.from<string | null>({ length: size }).fill(null)
  );
  const red = "#ff0000";
  const faint = "#cc000080";
  const cx = 7.5;
  const cy = 7.5;

  for (let y = 0; y < size; y++) {
    for (let x = 0; x < size; x++) {
      // 中心十字線
      if ((x === 7 || x === 8) && (y <= 3 || y >= 12)) {
        grid[y][x] = red;
      }
      if ((y === 7 || y === 8) && (x <= 3 || x >= 12)) {
        grid[y][x] = red;
      }
      // 中心ドット
      if ((x === 7 || x === 8) && (y === 7 || y === 8)) {
        grid[y][x] = red;
      }
      // 外周円（半径~7）
      const dx = x + 0.5 - cx;
      const dy = y + 0.5 - cy;
      const dist = Math.sqrt(dx * dx + dy * dy);
      if (dist >= 6.5 && dist <= 7.5 && grid[y][x] === null) {
        grid[y][x] = faint;
      }
    }
  }
  return grid;
})();

